    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed

        try {
            double tot = Double.valueOf(lblTotal.getText());
            double paid = Double.valueOf( edtCash.getText());
            if ( paid < tot ) {
                error("Please adapt your Paid amount.");
                edtCash.requestFocusInWindow();
                edtCash.selectAll();
                return ;
            }
            double balance = paid - tot ;
            
            DecimalFormat df = new DecimalFormat("00.00") ;
            
            lblRetour.setText(String.valueOf(df.format(balance)));
            
            productModel.saveToFile( this.title );
        } catch (IOException ex) {
            error(ex.getMessage() );
        }
        
    }//GEN-LAST:event_btnPayActionPerformed

    private void catListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_catListValueChanged
        Category cat = catList.getSelectedValue();
        pnlCategoryProducts.removeAll();
        
        cat.forEach( p -> {
            ProductButton pb = new ProductButton(p);
            pb.addActionListener( new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    try {
                        addProduct(pb.getProduct());
                    } catch (CloneNotSupportedException ex) {
                        Logger.getLogger(TableContentDialog.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } );
            pnlCategoryProducts.add( pb );
        } );
        
        pnlCategoryProducts.revalidate();
        pnlCategoryProducts.repaint();
        
    }//GEN-LAST:event_catListValueChanged

    private void btnIncByOneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncByOneActionPerformed
        if ( productModel.isEmpty() ) {
            return ;
        }
        int row = productTable.getSelectedRow();
        if ( row == -1 ) {
            error("Please Select a Row in the Table." );
            return ;
        }
        String productId = productModel.getValueAt( row, 0 ).toString();
        Product product = productModel.getProductByID(Integer.valueOf( productId ));
        product.incByOne();
        productModel.fireTableDataChanged();
        calcTotal();
        if ( !productModel.products().isEmpty() ) {
            productTable.setRowSelectionInterval( row, row);
        }
    }//GEN-LAST:event_btnIncByOneActionPerformed

    private void btnDecByOneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecByOneActionPerformed
        if ( productModel.isEmpty() ) {
            return ;
        }
        int row = productTable.getSelectedRow();
        if ( row == -1 ) {
            error("Please Select a Row in the Table." );
            return ;
        }
        String productId = productModel.getValueAt( row, 0 ).toString();
        Product product = productModel.getProductByID(Integer.valueOf( productId ));
        
        if ( product.getQtn() == 1 ) {
            productModel.remove(row);
        }
        
        product.decrementByOne();
        productModel.fireTableDataChanged();
        
        if ( product.getQtn()>=1 ) {
                productTable.setRowSelectionInterval( row, row);
        }
        
        calcTotal();
    }//GEN-LAST:event_btnDecByOneActionPerformed


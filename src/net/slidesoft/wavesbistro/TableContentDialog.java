    public void addProduct( Product product ) throws CloneNotSupportedException{
        if ( productModel.products().isEmpty() ) {
            this.productModel.add( product );
        } else {
            Product prod = productModel.getProductByID( product.id );
            if ( null == prod ) {
                this.productModel.add( product );
            } else{
                prod.incByOne();
            }
            this.productModel.fireTableDataChanged();
        }
        
        calcTotal();
    }

    private void calcTotal() {
       double tot =   this.productModel.products().stream().mapToDouble( Product::getPriceWithQtn ).sum();
       tot = (float) (Math.round(tot * 100) / 100.0);
       final DecimalFormat df = new DecimalFormat("00.00") ;
       lblTotal.setText( df.format( tot ) );
   }
    
